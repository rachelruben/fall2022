package module1._2variables;

public class VariableAssignment
{
   public static void main(String[] args)
   {
     int score;
     score = 6;
     System.out.println(score);

     double price = 43.25;
     System.out.println(price);

     boolean won = true;
     System.out.println(won);
     won = true;
     System.out.println(won);

     String name = "Charles";
     System.out.println(name);
   }
}

